package com.code_lama.recyclerview;

public class Kisi {

    private String isim;
    private String numara;
    private int resim;

    public Kisi() {
    }

    public String getIsim() {
        return isim;
    }

    public Kisi setIsim(String isim) {
        this.isim = isim;
        return this;
    }

    public String getNumara() {
        return numara;
    }

    public Kisi setNumara(String numara) {
        this.numara = numara;
        return this;
    }

    public int getResim() {
        return resim;
    }

    public Kisi setResim(int resim) {
        this.resim = resim;
        return this;
    }
}
